//1. Сумата на 3 числа
// Напишете JS функция, която да приема три числа като параметри и да извежда тяхната сума.
// Входът идва като масив от низови елементи, които трябва да бъдат обърнати към числа.
// Изходът трябва да бъде отпечатан на конзолата.

function sum(arr){
    let num1 = Number(arr[0]);
    let num2 = Number(arr[1]);
    let num3 = Number(arr[2]);

    let sum = num1 + num2 + num3;
    console.log('Sum: ' + sum);
}
sum(['2', '3', '4']);